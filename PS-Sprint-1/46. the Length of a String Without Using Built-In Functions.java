import java.util.*;
public class Main
{
	public static void main(String[] args) {
	    Scanner sc = new Scanner(System.in);
	    
	    System.out.print("Enter word : ");
	    String str = sc.nextLine();
	    int length = 0;
	    
	    try{
	        while(true){
	        str.charAt(length);
	        length ++;
	        }
	    }
	    catch(Exception e){
	        
	    }
	    
	    System.out.println("Length : " + length);
	}
}
	    
